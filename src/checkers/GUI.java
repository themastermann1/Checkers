    public void movePiece(Position pos){
        if(firstMove){
            //this is the first pos of the move
            Position pos1 = pos;
            firstMove = false;            
            
        }else if (firstMove == false){
            //this is the second move
            Position pos2 = pos;
            Move m = new Move(pos1, pos2);
            currentMove = m;
            
        } 
    }
    
